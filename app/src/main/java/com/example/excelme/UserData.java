package com.example.excelme;

public class UserData {

    public String name;
    public String email, height, weight, birthday;

    public UserData() {

    }

    public UserData(String name, String email, String height, String weight, String birthday) {
        this.name = name;
        this.email = email;
        this.height = height;
        this.weight = weight;
        this.birthday = birthday;
    }


}
